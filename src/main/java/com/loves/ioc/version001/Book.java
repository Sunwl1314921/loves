package com.loves.ioc.version001;

import lombok.Data;

import java.io.Serializable;

@Data
public class Book implements Serializable {

    private int id;

    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
